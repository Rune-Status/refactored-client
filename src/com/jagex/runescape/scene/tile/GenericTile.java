package com.jagex.runescape.scene.tile;

public class GenericTile {

	public GenericTile(int i, int j, int k, int l, int texture, int rgbColor, boolean flat) {
		anInt95 = i;
		anInt96 = j;
		anInt97 = k;
		anInt98 = l;
		this.texture = texture;
		this.rgbColor = rgbColor;
		this.flat = flat;
	}

	public int anInt95;
	public int anInt96;
	public int anInt97;
	public int anInt98;
	public int texture;
	public boolean flat;
	public int rgbColor;
}
